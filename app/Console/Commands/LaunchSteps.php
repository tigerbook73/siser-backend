class PriceBeautify
{
  public static function beautifyToInt(float $in): float
  {
    if ($in < 50) {
      return self::roundUnder50($in);
    }
    return self::round50AndGreater($in);
  }


  /**
   * the last digit 
   * 0 - Round down to 9
   * 1 - Round down to 9
   * 2 - Round down to 9
   * 3 - Round down to 9
   * 4 - Round down to 9
   * 5 - Leave at 5
   * 6 - Round down to 5
   * 7 - Round down to 5
   * 8 - Round down to 5
   * 9 - Leave at 9
   */
  public static function round50AndGreater(float $in): float
  {
    $result    = round($in);
    $lastDigit = $result % 10;
    $result    = floor($result / 10) * 10;

    if ($lastDigit <= 4) {
      $result = $result - 1;
    } elseif ($lastDigit <= 8) {
      $result = $result + 5;
    } else {
      $result = $result + 9;
    }

    return $result;
  }


  /**
   * 0 - Leave at 0
   * 1 - Round down to 0
   * 2 - Leave at 2
   * 3 - Leave at 3
   * 4 - Leave at 4
   * 5 - Leave at 5
   * 6 - Round down to 5
   * 7 - Leave at 7
   * 8 - Leave at 8
   * 9 - Leave at 9
   */
  public static function roundUnder50(float $in): float
  {
    $result = round($in);
    if ($result < 2) {

      return $result;
    }

    $lastDigit = $result % 10;

    if ($lastDigit === 1) {
      $result = $result - 1;
    } elseif ($lastDigit === 6) {
      $result = $result - 1;
    }

    return $result;
  }
}


